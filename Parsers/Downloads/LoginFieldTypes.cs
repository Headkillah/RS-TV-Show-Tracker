namespace RoliSoft.TVShowTracker.Parsers.Downloads
{
    /// <summary>
    /// Represents login field types.
    /// </summary>
    public enum LoginFieldTypes
    {
        /// <summary>
        /// Represents the username.
        /// </summary>
        UserName,
        /// <summary>
        /// Represents the password.
        /// </summary>
        Password,
        /// <summary>
        /// Represents an URL to return to after login.
        /// </summary>
        ReturnTo,
        /// <summary>
        /// Represents a CAPTCHA entry.
        /// </summary>
        Captcha
    }
}
